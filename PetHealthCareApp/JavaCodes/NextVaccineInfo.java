package com.berkaygunay.finalproject;

public class NextVaccineInfo {

    public String date;
    public String day;
    public String month;
    public String year;
    public String hour;
    public String minute;
    public String vaccine;
    public String count;

    public NextVaccineInfo(String date, String day, String month, String year, String hour, String minute,String vaccine,String count) {
        this.date = date;
        this.day = day;
        this.month = month;
        this.year = year;
        this.hour = hour;
        this.minute = minute;
        this.vaccine = vaccine;
        this.count = count;
    }
}
